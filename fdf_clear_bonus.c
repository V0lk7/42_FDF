#include "fdf.h"

void	ft_clear(t_vars *vars)
{
	int	i;
	int	j;

	i = 0;
	while (i < vars->y)
	{
		j = 0;
		while (j < vars->x)
		{
			ft_pixel_put(vars, j, i, 0x000000);
			j++;
		}
		i++;
	}
	return ;
}
